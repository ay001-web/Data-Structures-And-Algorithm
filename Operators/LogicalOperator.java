package Operators;
public class LogicalOperator {
    public static void main(String args[]) {
        /* 
        Logical AND
        System.out.println((3>2)&&(5>0));
        */


       /* 
        Logical OR
        System.out.println((3<2)||(3>2));
        */


        /* 
        Logical NOT
       System.out.println(!(3>2));

       */
    }
    
}
