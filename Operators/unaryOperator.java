package Operators;
public class unaryOperator {
    public static void main (String args[]) {
        /*  
        Pre increment
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
        */
        /*
        Post increment
        int a = 10;
        int b = a++;
        System.out.println(a);
        System.out.println(b);
        */
        /* 
        Pre decrement
        int a = 10;
        int b =--a;
        System.out.println(a);
        System.out.println(b);
        */
        /* 
        Post decrement
        int a = 10;
        int b = a--;
        System.out.println(a);
        System.out.println(b);
        */
    }
}